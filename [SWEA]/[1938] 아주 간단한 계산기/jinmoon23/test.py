num1,num2 = map(int,input().split())

def add_cal(num1,num2):
    return num1+num2
def sub_cal(num1,num2):
    return num1-num2
def mul_cal(num1,num2):
    return num1*num2
def div_cal(num1,num2):
    return num1//num2

print(add_cal(num1,num2))
print(sub_cal(num1,num2))
print(mul_cal(num1,num2))
print(div_cal(num1,num2))            